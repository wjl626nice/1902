# 猴子吃桃问题
'''
猴子第一天摘下若干桃子，每天吃一半加一个，到 m 天还剩 n 个。问第一天总共摘了total个桃子

要求：用户输入 m 和 n, 输出 total
'''

m = int(input("请输入天:"))
n = int(input("请输入剩余个数:"))

'''
m 5
n 4

    天       余
    m        n
    5        4
    4        (4+1)*2
    3        ((4+1)*2+1)*2
    2        (((4+1)*2+1)*2+1)*2
    1        ((((4+1)*2+1)*2+1)*2+1)*2
    0        (((((4+1)*2+1)*2+1)*2+1)*2+1)*2

'''
total = 0
while m > 0:
    n = (n + 1) * 2
    m -= 1
else:
    total = n

print(total)
